
public class User {
	int id;
	String name;
	String email;
	String phone;
	String idType;
	String idNumber;
	String dlNumber;
	User(int id, String name, String email, String phone, String idType, String idNumber, String dlNumber){
		this.id = id;
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.idType = idType;
		this.idNumber = idNumber;
		this.dlNumber = dlNumber;
	}
}
