
public class Car {
	int id;
	String brand;
	String model;
	String color;
	String number;
	int value;
	double rate;
	Car(int id, String brand, String model, String color, String number, int value, double rate){
		this.id = id;
		this.brand = brand;
		this.model = model;
		this.color = color;
		this.number = number;
		this.value = value;
		this.rate = rate;
	}
}
